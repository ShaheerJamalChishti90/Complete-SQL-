CREATE DATABASE OFFICE;
USE OFFICE;

-- Basics of creating a table and using it
CREATE TABLE EMPLOYEE(
	EMPID INT PRIMARY KEY,
	FIRSTNAME VARCHAR(50),
	LASTNAME VARCHAR(50), 
	AGE INT,  
	DEPT VARCHAR(225)
);


