-- Basic select statement with some filteration
SELECT 
	EMPID, CONCAT(FIRSTNAME, ' ', LASTNAME) EMPLOYEE_NAME, AGE, DEPT 
FROM 
	EMPLOYEE 
WHERE 
	DEPT = 'CYBERSECURITY' AND FIRSTNAME LIKE 'M%'
ORDER BY EMPID;

